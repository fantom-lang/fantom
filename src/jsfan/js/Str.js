/**
 * Str
 */
var sys_Str = sys_Obj.extend(
{

//////////////////////////////////////////////////////////////////////////
// Constructor
//////////////////////////////////////////////////////////////////////////

  $ctor: function() {},

//////////////////////////////////////////////////////////////////////////
// Methods
//////////////////////////////////////////////////////////////////////////

  type: function()
  {
    return sys_Type.find("sys::Str");
  },

});

//////////////////////////////////////////////////////////////////////////
// Static Methods
//////////////////////////////////////////////////////////////////////////

sys_Str.contains = function(self, arg) { return self.indexOf(arg) != -1 }
sys_Str.each = function(self, func)
{
  for (var i=0; i<self.length; i++)
    func(self.charAt(i), i);
}

