/**
 * Uri
 */
var sys_Uri = sys_Obj.extend(
{

//////////////////////////////////////////////////////////////////////////
// Constructor
//////////////////////////////////////////////////////////////////////////

  $ctor: function(uri)
  {
    this.m_uri = uri;
  },

//////////////////////////////////////////////////////////////////////////
// Identity
//////////////////////////////////////////////////////////////////////////

  type: function()
  {
    return sys_Type.find("sys::Uri");
  },

  toStr: function()
  {
    return m_uri;
  },

//////////////////////////////////////////////////////////////////////////
// Fields
//////////////////////////////////////////////////////////////////////////

  m_uri: ""

});

//////////////////////////////////////////////////////////////////////////
// Static Methods
//////////////////////////////////////////////////////////////////////////

sys_Uri.make = function(uri)
{
  return new sys_Uri(uri);
}

sys_Uri.fromStr = function(s)
{
  return new sys_Uri(uri);
}
